    private void btn_prikaziPorukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_prikaziPorukuActionPerformed
        
        int row = tbl_poslednjeTriPoruke.getSelectedRow();
        
        if (row != -1) {
            ModelTabelePoruke mt = (ModelTabelePoruke) tbl_poslednjeTriPoruke.getModel();
            String poruka = mt.vratiPoruku(row);
            
            FormaPoruka fp = new FormaPoruka(this, true, poruka);
            fp.setVisible(true);
        }
        
        
    }//GEN-LAST:event_btn_prikaziPorukuActionPerformed

