    private void btn_posaljiIzabranomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_posaljiIzabranomActionPerformed
        
        String poruka = txt_poruka.getText();
        String usernamePrimaoca = txt_korisnik.getText();
        
        if ("Izaberi korisnika iz liste ulogovanih".equals(usernamePrimaoca)) {
            JOptionPane.showMessageDialog(this, "Morate izabrati korisnika kome saljete poruku!");
            return;
        }
        Poruka p = new Poruka(-1, ulogovani.getUsername(), usernamePrimaoca, new Date(), poruka);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacije(Operacije.SACUVAJ_PORUKU);
        kz.setParametar(p);
        
        Komunikacija.getInstance().posaljiZahtev(kz);
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        
        boolean uspesno = (boolean) so.getOdgovor();
        
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Poruka je uspesno poslata!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Doslo je do greske prilikom slanja poruke!");
        }
        
    }//GEN-LAST:event_btn_posaljiIzabranomActionPerformed

